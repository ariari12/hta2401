package ITBookFleaMarket.vo;

public class RouletteVO {
	private int prizeno;
	private int prizeMile;
	private int winningpct;
	private int stoke;
	
	public int getPrizeno() {
		return prizeno;
	}
	public void setPrizeno(int prizeno) {
		this.prizeno = prizeno;
	}
	public int getPrizeMile() {
		return prizeMile;
	}
	public void setPrizeMile(int prizeMile) {
		this.prizeMile = prizeMile;
	}
	public int getWinningpct() {
		return winningpct;
	}
	public void setWinningpct(int winningpct) {
		this.winningpct = winningpct;
	}
	public int getStoke() {
		return stoke;
	}
	public void setStoke(int stoke) {
		this.stoke = stoke;
	}
	
}
